/***********************************************************************************/
#define DISCONNECT_OC2					0
#define NON_INVERTING_OC2				1
#define INVERTING_OC2					2
/***********************************************************************************/


/***********************************************************************************/
/*         OUTPUT COMPARE PIN (OC2) BEHAVIOUR IN CASE OF PHASE CORRECT MODE        */
/***********************************************************************************/
#define DISCONNECT_OC2					0
#define NON_INVERTING_OC2				1
#define INVERTING_OC2					2
/***********************************************************************************/


/***********************************************************************************/
